package model;

/**
 * This interface is to maintain the Pixel State.
 */
public interface IPixelState {

  /**
   * This is a getter method to return the red component.
   *
   * @return red component.
   */
  int getR();

  /**
   * This is a getter method to return the green component.
   *
   * @return green component.
   */
  int getG();

  /**
   * This is a getter method to return the blue component.
   *
   * @return blue component.
   */
  int getB();

}
